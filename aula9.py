# operadores matemáticas

adicao = 10 + 10
print(adicao)

sub = 10 - 5
print(sub)

mult = 10 * 10
print(mult)

div = 100 / 5.2  # independente da classe, sempre aparece float
print(div)

div_int = 100 // 5.2
print(div_int, type(div_int))

exponenciacao = 2 ** 10
print('Exponenciação:', exponenciacao)

modulo = 55 % 2 # resto da divisão
print(modulo)